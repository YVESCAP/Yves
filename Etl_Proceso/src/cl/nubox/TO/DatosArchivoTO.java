package cl.nubox.TO;

public class DatosArchivoTO {
	
	private String metaScore;
	private String nombreJuego;
	private String nombreConsola;
	private String notaUsuario;
	private String fechaNota;
	
	public String getMetaScore() {
		return metaScore;
	}
	public void setMetaScore(String metaScore) {
		this.metaScore = metaScore;
	}
	public String getNombreJuego() {
		return nombreJuego;
	}
	public void setNombreJuego(String nombreJuego) {
		this.nombreJuego = nombreJuego;
	}
	public String getNombreConsola() {
		return nombreConsola;
	}
	public void setNombreConsola(String nombreConsola) {
		this.nombreConsola = nombreConsola;
	}
	public String getNotaUsuario() {
		return notaUsuario;
	}
	public void setNotaUsuario(String notaUsuario) {
		this.notaUsuario = notaUsuario;
	}
	public String getFechaNota() {
		return fechaNota;
	}
	public void setFechaNota(String fechaNota) {
		this.fechaNota = fechaNota;
	}
	

}
